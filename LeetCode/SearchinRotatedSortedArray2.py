class Solution:
    # @param A a list of integers
    # @param target an integer
    # @return a boolean
    def search(self, A, target):
        if(A == []):
            return False
        left=0
        right=len(A)-1
        while(left <= right):
            mid=(right-left)/2+left
            if(target == A[mid]):
                return True
            if(A[left] == A[mid] and A[right] == A[mid]):
            	return self.search(A[left:mid],target) or self.search(A[mid:right],target)
            if(A[left] <= A[mid] ):
                if(target <= A[mid] and target >= A[left]):
                    right=mid-1
                else:
                    left=mid+1
            else:
                if(target <= A[right] and target >=A[mid]):
                    left=mid+1
                else:
                    right=mid-1
        return False

